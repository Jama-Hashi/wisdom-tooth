// Card flipper function
let flipped = false;
export const handleClick = async (
  proverb,
  setFlipIn,
  setFlipOut,
  setDisplayedWritng
) => {
  if (!flipped) {
    flipped = true;
    await new Promise((resolve) => {
      setFlipIn(true);
      setTimeout(() => {
        setDisplayedWritng(proverb.proverb);
        resolve();
      }, 250);
    })
      .then(() => {
        setFlipOut(true);
        return new Promise((resolve) => {
          setTimeout(() => {
            setFlipIn(false);
            setFlipOut(false);
            resolve();
          }, 250);
        });
      })
      .then(() => {
        return new Promise((resolve) => {
          setTimeout(() => {
            setFlipIn(true);
            resolve();
          }, 5000);
        });
      })
      .then(() => {
        return new Promise((resolve) => {
          setTimeout(() => {
            setDisplayedWritng(proverb.hint);
            resolve();
          }, 250);
        });
      })
      .then(() => {
        setFlipOut(true);
        setTimeout(() => {
          flipped = false;
          setFlipIn(false);
          setFlipOut(false);
        }, 250);
      });
  }
};
